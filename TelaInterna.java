    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String cpfBusca = jTextField6.getText().trim(); 
        String pesoTexto = jTextField7.getText().trim();

    // Verificamos se ambos os campos estão preenchidos
    if (cpfBusca.isEmpty() || pesoTexto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Atenção: Preencha o CPF e o Novo Peso!");
        return; 
    }

    try {
   
        double novoPeso = Double.parseDouble(pesoTexto);
        boolean alunoEncontrado = false;

        for (Aluno a : listaAlunos) {
            // Se o CPF do aluno na lista for igual ao CPF digitado
            if (a.cpf.equals(cpfBusca)) {
                a.peso = novoPeso; // ATUALIZA o peso do objeto na memória
                javax.swing.JOptionPane.showMessageDialog(null, "Sucesso! O novo peso de " + a.nome + " é: " + a.peso + "kg");
                alunoEncontrado = true;
                break; // Para o laço porque já encontrou o aluno
            }
        }

        //Se terminou o laço e não encontrou ninguém
        if (!alunoEncontrado) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro: Aluno com o CPF " + cpfBusca + " não existe.");
        }

        // Limpa os campos para a próxima atualização
        jTextField6.setText("");
        jTextField7.setText("");

    } catch (NumberFormatException e) {
        // Se o usuário digitou letras no peso ou usou vírgula
        javax.swing.JOptionPane.showMessageDialog(null, "No campo Peso, use apenas números e ponto (Ex: 80.5).");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String cpfParaRemover =  jTextField8.getText().trim();

    //Valida se o campo não está vazio
    if (cpfParaRemover.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, digite um CPF para remover!");
        return;
    }
    
    //Cria uma variável para controlar se o aluno foi achado
    boolean excluido = false;

    for (int i = 0; i < listaAlunos.size(); i++) {
        if (listaAlunos.get(i).cpf.equals(cpfParaRemover)) {
            String nomeRemovido = listaAlunos.get(i).nome; // Guarda o nome para o aviso
            listaAlunos.remove(i); // Remove o objeto da lista
            javax.swing.JOptionPane.showMessageDialog(null, "O aluno " + nomeRemovido + " foi removido com sucesso!");
            excluido = true;
            break; // Sai do laço após remover
        }
    }

    //Se não encontrar o CPF
    if (!excluido) {
        javax.swing.JOptionPane.showMessageDialog(null, "Erro: CPF " + cpfParaRemover + " não encontrado no sistema.");
    }

    // Limpa o campo
    jTextField8.setText(""); 
        
    }//GEN-LAST:event_jButton4ActionPerformed


