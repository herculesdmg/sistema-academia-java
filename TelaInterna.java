    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
        Aluno novoAluno = new Aluno();   
        novoAluno.nome = jTextField1.getText(); 
        novoAluno.cpf = jTextField2.getText();
        novoAluno.idade = Integer.parseInt(jTextField3.getText());
        novoAluno.peso = Double.parseDouble(jTextField4.getText());
        novoAluno.objetivo = jTextField5.getText();

        // Chama o método de validação que criamos na classe Aluno
        if (novoAluno.dadosEstaoValidos()) {
            listaAlunos.add(novoAluno); 
            novoAluno.seApresentar();  
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Preencha Nome, CPF e Idade corretamente!");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Erro: Idade e Peso devem ser números.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

